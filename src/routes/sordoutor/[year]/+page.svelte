<script lang="ts">
	import type { PageProps } from './$types';
	import { fade, fly, scale } from 'svelte/transition';
	import confetti from 'canvas-confetti';
	import { tweened } from 'svelte/motion';
	import { linear } from 'svelte/easing';

	let { data }: PageProps = $props();

	let step = $state(0);
	let maxStep = $derived(data.data.length - 1);
	let finished = $state(false);
	let score = $state(0);
	let sordoutor = $derived(data.data[step]);
	let selectedAnswer = $state('');
	let explanationRef: HTMLElement;
	let autoAdvanceTimer: ReturnType<typeof setTimeout>;

	// Time in milliseconds for auto-advance
	const autoAdvanceTime = 4000;

	// Create a tweened progress value for the button fill
	const progress = tweened(0, {
		duration: autoAdvanceTime,
		easing: linear
	});

	function incrementStep() {
		if (selectedAnswer === '') return;

		// Clear any existing timer when manually advancing
		if (autoAdvanceTimer) {
			clearTimeout(autoAdvanceTimer);
		}

		// Reset progress
		progress.set(0);

		if (step < maxStep) {
			step += 1;
			selectedAnswer = '';
		} else {
			finished = true;
			if (score > maxStep / 2) {
				triggerConfetti();
			}
		}
	}

	function checkAnswer(isDoutor: boolean) {
		selectedAnswer = isDoutor ? 'doutor' : 'plebeu';
		if ((isDoutor && sordoutor.sordoutor) || (!isDoutor && !sordoutor.sordoutor)) {
			score += 1;
		}

		// Start the progress animation
		progress.set(1);

		// Set a timer to automatically advance after the set time
		autoAdvanceTimer = setTimeout(incrementStep, autoAdvanceTime);

		// Scroll to explanation after a short delay
		setTimeout(() => {
			if (explanationRef) {
				explanationRef.scrollIntoView({ behavior: 'smooth', block: 'center' });
			}
		}, 100);
	}

	function triggerConfetti() {
		confetti({
			particleCount: 100,
			spread: 70,
			origin: { y: 0.6 }
		});
	}

	function restartGame() {
		step = 0;
		score = 0;
		finished = false;
		selectedAnswer = '';

		// Reset progress
		progress.set(0);

		if (autoAdvanceTimer) {
			clearTimeout(autoAdvanceTimer);
		}
	}

	let subtitle = $derived(() => {
		if (sordoutor.sexo === 'masculino') {
			return `O ${sordoutor.name} √© S√¥r Doutor?`;
		} else {
			return `A ${sordoutor.name} √© S√¥r Doutora?`;
		}
	});
</script>

<div class="min-h-screen bg-gradient-to-b from-indigo-50 to-blue-100 px-4 py-12">
	<div
		class="mx-auto flex max-w-2xl flex-col items-center justify-center rounded-2xl bg-white p-8 shadow-lg"
	>
		{#if !finished}
			<div class="mb-6 flex w-full items-center justify-between">
				<div class="rounded-full bg-blue-100 px-4 py-2">
					<span class="font-medium">Quest√£o {step + 1}/{maxStep + 1}</span>
				</div>
				<div class="rounded-full bg-blue-100 px-4 py-2">
					<span class="font-medium">Pontua√ß√£o: {score}</span>
				</div>
			</div>

			<h1 class="mb-8 text-center text-3xl font-bold text-indigo-800" in:fade={{ duration: 400 }}>
				{subtitle()}
			</h1>

			<div
				class="relative mb-8 w-full overflow-hidden rounded-xl shadow-md"
				style="max-height: 400px;"
				in:scale={{ duration: 400, start: 0.9, opacity: 0 }}
			>
				<img
					class="h-full w-full object-cover object-center"
					src={sordoutor.img}
					alt={sordoutor.name}
				/>
			</div>

			<div class="flex w-full flex-col items-center justify-center gap-4 sm:flex-row">
				<button
					class="w-full rounded-lg bg-indigo-600 px-6 py-3 text-lg font-semibold text-white shadow-md transition-all hover:bg-indigo-700 disabled:cursor-not-allowed disabled:opacity-50 sm:w-auto"
					class:ring-4={selectedAnswer === 'doutor'}
					class:ring-green-500={selectedAnswer === 'doutor' && sordoutor.sordoutor}
					class:ring-red-500={selectedAnswer === 'doutor' && !sordoutor.sordoutor}
					onclick={() => checkAnswer(true)}
					disabled={selectedAnswer !== ''}
				>
					S√¥r Doutor
				</button>
				<button
					class="w-full rounded-lg bg-gray-700 px-6 py-3 text-lg font-semibold text-white shadow-md transition-all hover:bg-gray-800 disabled:cursor-not-allowed disabled:opacity-50 sm:w-auto"
					class:ring-4={selectedAnswer === 'plebeu'}
					class:ring-green-500={selectedAnswer === 'plebeu' && !sordoutor.sordoutor}
					class:ring-red-500={selectedAnswer === 'plebeu' && sordoutor.sordoutor}
					onclick={() => checkAnswer(false)}
					disabled={selectedAnswer !== ''}
				>
					Plebeu
				</button>
			</div>

			{#if selectedAnswer !== ''}
				<div class="mt-6" in:fly={{ y: 20, duration: 300 }} bind:this={explanationRef}>
					<div class="rounded-lg border-l-4 border-indigo-500 bg-slate-50 p-4">
						<p class="mb-2 font-medium">Segundo a Wikipedia:</p>
						<p class="text-gray-700">
							{@html sordoutor.summary}
						</p>
						<a
							href={sordoutor.wikipedia}
							target="_blank"
							rel="noopener noreferrer"
							class="mt-2 inline-block text-sm text-indigo-600 hover:text-indigo-800"
						>
							Ver na Wikipedia ‚Üí
						</a>
					</div>

					<div class="mt-4 flex justify-center">
						<button
							class="relative overflow-hidden rounded-lg bg-indigo-600 px-6 py-2 text-white shadow-md transition-all hover:bg-indigo-700"
							onclick={incrementStep}
						>
							<div
								class="absolute inset-0 left-0 bg-indigo-400 opacity-30"
								style="width: {$progress * 100}%"
							></div>
							<span class="relative z-10">Pr√≥xima Quest√£o</span>
						</button>
					</div>
				</div>
			{/if}
		{:else}
			<div in:scale={{ duration: 600, delay: 300 }}>
				<h1 class="mb-4 text-center text-3xl font-bold text-indigo-800">Fim do Jogo!</h1>

				<div class="mb-6 rounded-xl bg-gradient-to-r from-indigo-100 to-blue-100 p-6">
					<p class="text-center text-2xl font-bold">
						Pontua√ß√£o final: {score}/{maxStep + 1}
					</p>
					<p class="mt-2 text-center">
						{#if score === maxStep + 1}
							üèÜ Perfeito! √âs um especialista em doutoramentos!
						{:else if score > maxStep / 2}
							üëè Muito bem! Tens bom olho para acad√©micos!
						{:else if score > 0}
							ü§î Continua a tentar, h√° espa√ßo para melhorar!
						{:else}
							üòÖ Ups! Talvez devas rever os teus conhecimentos sobre doutoramentos!
						{/if}
					</p>
				</div>

				<button
					class="w-full rounded-lg bg-indigo-600 px-6 py-3 text-lg font-semibold text-white shadow-md transition-all hover:bg-indigo-700"
					onclick={restartGame}
				>
					Jogar Novamente
				</button>
			</div>
		{/if}
	</div>
</div>
